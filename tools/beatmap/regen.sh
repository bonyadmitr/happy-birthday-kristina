#!/usr/bin/env bash
# ============================================================================
#  Пересобрать карту битов ритм-игры (js/rhythm-beatmap.js).
#
#  Использование:
#     tools/beatmap/regen.sh [MIN_GAP] [TOP]
#
#  MIN_GAP — минимальная пауза между нотами, сек. БОЛЬШЕ = МЕНЬШЕ нот. (по умолч. 0.55)
#  TOP     — доля самых сильных акцентов песни, 0..1. БОЛЬШЕ = БОЛЬШЕ нот. (по умолч. 0.70)
#
#  Хочешь БОЛЬШЕ нот  → подними TOP (напр. 0.85) и/или снизь MIN_GAP (напр. 0.45).
#  Хочешь МЕНЬШЕ нот  → снизь TOP (напр. 0.50) и/или подними MIN_GAP (напр. 0.80).
#
#  Примеры:
#     tools/beatmap/regen.sh              # дефолт (69 нот)
#     tools/beatmap/regen.sh 0.45 0.85    # заметно плотнее
#     tools/beatmap/regen.sh 0.80 0.50    # разреженнее, больше пауз
#
#  Скрипт печатает итоговое число нот. После пересборки — обнови страницу
#  (при локальной отладке service worker может отдавать старое: см. CLAUDE.md).
# ============================================================================
set -e
cd "$(dirname "$0")/../.."

AUDIO="assets/audio/mary-on-a-cross-loop.mp3"   # сменил песню → поправь путь тут
MIN_GAP="${1:-0.55}"
TOP="${2:-0.70}"

tools/beatmap/.venv/bin/python tools/beatmap/extract.py \
  "$AUDIO" --src "$AUDIO" \
  --out js/rhythm-beatmap.js --accent --min-gap "$MIN_GAP" --top "$TOP"
